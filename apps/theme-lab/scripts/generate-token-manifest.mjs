import { mkdir, readFile, readdir, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const labRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const repoRoot = path.resolve(labRoot, '../..');
const themeSourceRoot = path.join(repoRoot, 'packages/themes/src');
const outputPath = path.join(labRoot, 'src/generated/token-manifest.ts');

const tokenGroupLabels = {
  core: 'Core palette',
  semantic: 'Semantic contract',
  surface: 'Surface and glass',
  atmosphere: 'Atmosphere',
  shape: 'Shape, shadow, type',
  tailwind: 'Tailwind aliases',
  other: 'Other tokens'
};

const semanticTokens = new Set([
  'background',
  'foreground',
  'card',
  'card-foreground',
  'muted',
  'muted-foreground',
  'border',
  'border-soft',
  'primary',
  'primary-foreground',
  'primary-soft',
  'accent-foreground',
  'accent-soft',
  'destructive',
  'good',
  'good-soft',
  'warn',
  'warn-soft'
]);

const coreTokens = new Set(['cream', 'pistachio', 'peach', 'lavender', 'ink', 'ink-2', 'ink-3', 'accent']);

async function listCssFiles(dir) {
  const entries = await readdir(dir, { withFileTypes: true });
  const files = await Promise.all(
    entries.map(async (entry) => {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) return listCssFiles(fullPath);
      if (entry.isFile() && entry.name.endsWith('.css')) return [fullPath];
      return [];
    })
  );

  return files.flat();
}

function categorize(name) {
  if (name.startsWith('color-') || name.startsWith('animate-')) return 'tailwind';
  if (coreTokens.has(name)) return 'core';
  if (semanticTokens.has(name)) return 'semantic';
  if (name.includes('rgb') || name === 'frost' || name.startsWith('glass-')) return 'surface';
  if (name.startsWith('blob-') || name === 'grain-opacity') return 'atmosphere';
  if (name.startsWith('radius') || name.startsWith('shadow') || name.startsWith('font')) return 'shape';
  return 'other';
}

function sortTokens(a, b) {
  const groupOrder = Object.keys(tokenGroupLabels);
  const groupDelta = groupOrder.indexOf(a.group) - groupOrder.indexOf(b.group);
  if (groupDelta !== 0) return groupDelta;
  return a.name.localeCompare(b.name);
}

const tokenMap = new Map();
const cssFiles = await listCssFiles(themeSourceRoot);

for (const cssFile of cssFiles) {
  const css = await readFile(cssFile, 'utf8');
  const relativePath = path.relative(repoRoot, cssFile);
  const variablePattern = /--([a-zA-Z0-9-]+)\s*:/g;

  for (const match of css.matchAll(variablePattern)) {
    const name = match[1];
    const existing = tokenMap.get(name);
    if (existing) {
      existing.sourceFiles.add(relativePath);
      continue;
    }

    tokenMap.set(name, {
      name,
      cssVar: `--${name}`,
      group: categorize(name),
      sourceFiles: new Set([relativePath])
    });
  }
}

const tokens = Array.from(tokenMap.values())
  .map((token) => ({
    ...token,
    sourceFiles: Array.from(token.sourceFiles).sort()
  }))
  .sort(sortTokens);

const generated = `// Generated by apps/theme-lab/scripts/generate-token-manifest.mjs.
// Run \`pnpm generate:tokens\` after changing package theme CSS.

export type TokenGroupId = ${Object.keys(tokenGroupLabels).map((group) => `'${group}'`).join(' | ')};

export interface TokenDefinition {
  name: string;
  cssVar: \`--\${string}\`;
  group: TokenGroupId;
  sourceFiles: string[];
}

export const tokenGroups: Record<TokenGroupId, string> = ${JSON.stringify(tokenGroupLabels, null, 2)} as const;

export const tokens: TokenDefinition[] = ${JSON.stringify(tokens, null, 2)} as TokenDefinition[];
`;

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, generated);
